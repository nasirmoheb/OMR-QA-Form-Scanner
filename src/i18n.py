class I18n:
    """Simple translation manager."""

    _lang: str = "en"
    _listeners: list[Any] = []

    @classmethod
    def set_language(cls, lang: str) -> None:
        """Switch active language and notify listeners."""
        if lang not in _TRANSLATIONS:
            logger.warning("Unknown language '%s', falling back to 'en'", lang)
            lang = "en"
        cls._lang = lang
        for callback in cls._listeners:
            try:
                callback()
            except Exception:
                logger.exception("I18n listener failed")

    @classmethod
    def get_language(cls) -> str:
        return cls._lang

    @classmethod
    def t(cls, key: str, **kwargs: Any) -> str:
        """Translate a key, optionally formatting it."""
        text = _TRANSLATIONS.get(cls._lang, _TRANSLATIONS["en"]).get(
            key, _TRANSLATIONS["en"].get(key, key)
        )
        if kwargs:
            try:
                text = text.format(**kwargs)
            except KeyError:
                pass
        
        # Apply RTL text processing for proper rendering
        if cls.is_rtl() and _RTL_SUPPORT and text:
            try:
                # Reshape Arabic/Dari/Pashto characters for proper joining
                reshaped = arabic_reshaper.reshape(text)
                # Apply bidirectional algorithm for correct display order
                text = get_display(reshaped)
            except Exception as e:
                logger.debug(f"RTL processing failed for '{key}': {e}")
        
        return text

    @classmethod
    def register_listener(cls, callback: Any) -> None:
        if callback not in cls._listeners:
            cls._listeners.append(callback)

    @classmethod
    def unregister_listener(cls, callback: Any) -> None:
        if callback in cls._listeners:
            cls._listeners.remove(callback)

    @classmethod
    def is_rtl(cls) -> bool:
        """Return True for right-to-left languages (Dari, Pashto)."""
        return cls._lang in ("fa", "ps")

    @classmethod
    def languages(cls) -> dict[str, str]:
        return {
            "en": "English",
            "fa": "دری (Dari)",
            "ps": "پښتو (Pashto)",
        }


# Aliases for convenience
def _(key: str, **kwargs: Any) -> str:
    return I18n.t(key, **kwargs)

def is_rtl() -> bool:
    return I18n.is_rtl()

def rtl_text(text: str) -> str:
    """Reshape and apply bidi algorithm to an arbitrary string for RTL display."""
    if not I18n.is_rtl() or not _RTL_SUPPORT or not text:
        return text
    try:
        reshaped = arabic_reshaper.reshape(text)
        return get_display(reshaped)
    except Exception:
        return text

def get_start() -> str:
    return "right" if is_rtl() else "left"

def get_end() -> str:
    return "left" if is_rtl() else "right"

def get_anchor() -> str:
    return "e" if is_rtl() else "w"

def get_compound() -> str:
    return "right" if is_rtl() else "left"
